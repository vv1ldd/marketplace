#!/usr/bin/env bash

set -euo pipefail

REPO_ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
FRONTEND_DIR="${MEANLY_FRONTEND_DIR:-${REPO_ROOT}/frontend}"
FRONTEND_ENV="${FRONTEND_DIR}/.env"
TUNNEL_NAME="${MEANLY_DEV_TUNNEL_NAME:-meanly-dev}"
TUNNEL_HOST="${MEANLY_DEV_TUNNEL_HOST:-meanly.one}"
DEV_PROFILE="${MEANLY_DEV_PROFILE:-global}"
DEV_PORT="${MEANLY_DEV_PORT:-3001}"
CLOUDFLARED_DIR="${MEANLY_CLOUDFLARED_DIR:-${HOME}/.cloudflared}"
RUNTIME_DIR="${MEANLY_TUNNEL_RUNTIME_DIR:-${REPO_ROOT}/.cloudflared}"
CONFIG_FILE="${MEANLY_TUNNEL_CONFIG:-${RUNTIME_DIR}/config.${TUNNEL_NAME}.yml}"

cloudflared_credentials_path() {
    local name="${1:-$TUNNEL_NAME}"
    if [ -f "${CLOUDFLARED_DIR}/${name}.json" ]; then
        printf '%s' "${CLOUDFLARED_DIR}/${name}.json"
        return 0
    fi
    find "${CLOUDFLARED_DIR}" -maxdepth 1 -name '*.json' -type f 2>/dev/null | head -n 1
}

profile_hosts() {
    case "$DEV_PROFILE" in
        global)
            printf '%s\n' "${TUNNEL_HOST:-meanly.one}" "www.meanly.one"
            ;;
        ru)
            printf '%s\n' "${TUNNEL_HOST:-meanly.ru}" "www.meanly.ru"
            ;;
        all)
            printf '%s\n' "meanly.one" "www.meanly.one" "meanly.ru" "www.meanly.ru"
            ;;
        *)
            printf '%s\n' "${TUNNEL_HOST:-meanly.one}"
            ;;
    esac
}

render_config() {
    local credentials="$1"
    cat <<EOF
# Generated by scripts/dev-tunnel.sh (${TUNNEL_NAME}, profile=${DEV_PROFILE})
tunnel: ${TUNNEL_NAME}
credentials-file: ${credentials}

ingress:
EOF
    while IFS= read -r hostname; do
        [ -z "$hostname" ] && continue
        cat <<EOF
  - hostname: ${hostname}
    service: http://127.0.0.1:${DEV_PORT}
EOF
    done < <(profile_hosts)
    cat <<EOF
  - service: http_status:404
EOF
}

require_cloudflared() {
    if ! command -v cloudflared >/dev/null 2>&1; then
        echo "Install cloudflared first: brew install cloudflared" >&2
        exit 1
    fi
}

cmd_write_config() {
    local credentials
    credentials="$(cloudflared_credentials_path)"
    if [ -z "$credentials" ]; then
        credentials="${CLOUDFLARED_DIR}/${TUNNEL_NAME}.json"
        echo "Note: credentials not found yet. Run: $0 setup" >&2
    fi
    mkdir -p "$RUNTIME_DIR"
    render_config "$credentials" > "$CONFIG_FILE"
    echo "Wrote ${CONFIG_FILE}"
    echo "Hostnames:"
    profile_hosts | sed 's/^/  - /'
}

cmd_sync_env() {
    local primary_host
    primary_host="$(profile_hosts | head -n 1)"
    local public_url="https://${primary_host}"
    local region="global"
    if [ "$DEV_PROFILE" = "ru" ]; then
        region="ru"
        "${REPO_ROOT}/scripts/regional-frontend-env.sh" ru
        return 0
    fi

    if [ "$DEV_PROFILE" = "all" ]; then
        region="global"
    fi

    if [ ! -f "$FRONTEND_ENV" ]; then
        cp "${FRONTEND_DIR}/.env.example" "$FRONTEND_ENV"
    fi
    upsert_env() {
        local key="$1"
        local value="$2"
        if grep -q "^${key}=" "$FRONTEND_ENV"; then
            sed -i '' "s|^${key}=.*|${key}=${value}|" "$FRONTEND_ENV"
        else
            printf '%s=%s\n' "$key" "$value" >> "$FRONTEND_ENV"
        fi
    }
    if [ "$DEV_PROFILE" = "global" ]; then
        "${REPO_ROOT}/scripts/regional-frontend-env.sh" global >/dev/null
    fi
    upsert_env "NEXT_PUBLIC_STOREFRONT_URL" "$public_url"
    upsert_env "NEXT_PUBLIC_SIMPLE_L1_URL" "$public_url"
    upsert_env "NEXT_PUBLIC_MARKETPLACE_REGION" "$region"
    if ! grep -q '^NEXT_PUBLIC_MARKETPLACE_API_URL=' "$FRONTEND_ENV"; then
        upsert_env "NEXT_PUBLIC_MARKETPLACE_API_URL" "https://api.meanly.test"
    fi
    echo "Updated ${FRONTEND_ENV}:"
    echo "  profile=${DEV_PROFILE}"
    echo "  NEXT_PUBLIC_STOREFRONT_URL=${public_url}"
    echo "  NEXT_PUBLIC_SIMPLE_L1_URL=${public_url}"
    if [ "$DEV_PROFILE" = "all" ]; then
        echo "  market selection follows request Host (meanly.one vs meanly.ru)"
    fi
    echo "Restart Next dev after env change."
}

cmd_setup() {
    require_cloudflared
    if [ ! -f "${CLOUDFLARED_DIR}/cert.pem" ]; then
        echo "Run once: cloudflared tunnel login"
        exit 1
    fi
    if [ -z "$(cloudflared_credentials_path)" ]; then
        echo "Creating tunnel ${TUNNEL_NAME}..."
        cloudflared tunnel create "$TUNNEL_NAME"
    fi
    while IFS= read -r hostname; do
        [ -z "$hostname" ] && continue
        echo "Routing DNS ${hostname} -> tunnel ${TUNNEL_NAME}..."
        if ! cloudflared tunnel route dns "$TUNNEL_NAME" "$hostname"; then
            echo "Note: DNS record for ${hostname} already exists. Run: $0 dns-help" >&2
        fi
    done < <(profile_hosts)
    cmd_write_config
    cmd_sync_env
    echo ""
    echo "Next:"
    echo "  cd ${FRONTEND_DIR} && npm run dev -- -H 127.0.0.1 -p ${DEV_PORT}"
    echo "  MEANLY_DEV_PROFILE=${DEV_PROFILE} $0 run"
    echo "  open https://$(profile_hosts | head -n 1)"
}

cmd_run() {
    require_cloudflared
    if [ ! -f "$CONFIG_FILE" ]; then
        cmd_write_config
    fi
    if ! curl -fsS "http://127.0.0.1:${DEV_PORT}/" >/dev/null 2>&1; then
        echo "Warning: nothing on http://127.0.0.1:${DEV_PORT} — start Next dev first." >&2
    fi
    if [ "${MEANLY_BETA_NOTIFY:-}" = "1" ]; then
        echo "Starting beta Telegram notifier in background..."
        "${REPO_ROOT}/scripts/dev-tunnel-notify.sh" watch &
        BETA_NOTIFY_PID=$!
        trap 'kill "${BETA_NOTIFY_PID}" 2>/dev/null || true' EXIT INT TERM
    fi
    echo "Tunnel ${TUNNEL_NAME} (profile=${DEV_PROFILE}) -> 127.0.0.1:${DEV_PORT}"
    profile_hosts | sed 's/^/  https:\/\//'
    exec cloudflared tunnel --config "$CONFIG_FILE" run "$TUNNEL_NAME"
}

cmd_status() {
    require_cloudflared
    echo "Profile:  ${DEV_PROFILE}"
    echo "Local:    http://127.0.0.1:${DEV_PORT}"
    echo "Tunnel:   ${TUNNEL_NAME}"
    echo "Config:   ${CONFIG_FILE}"
    echo "Hostnames:"
    profile_hosts | sed 's/^/  https:\/\//'
    cloudflared tunnel list 2>/dev/null || true
    if curl -fsS "http://127.0.0.1:${DEV_PORT}/backend/healthcheck" >/dev/null 2>&1; then
        echo "Backend proxy: OK (/backend/healthcheck)"
    else
        echo "Backend proxy: not reachable (is Next dev running?)"
    fi
    if curl -fsS "http://127.0.0.1:3000/manifest.webmanifest" >/dev/null 2>&1; then
        echo "Simple L1 runtime: OK (127.0.0.1:3000)"
    else
        echo "Simple L1 runtime: not reachable on 127.0.0.1:3000 (identity proofs will fail)"
    fi
}

cmd_dns_help() {
    require_cloudflared
    local tunnel_id
    tunnel_id="$(cloudflared tunnel list 2>/dev/null | awk -v name="$TUNNEL_NAME" '$2 == name { print $1; exit }')"
    if [ -z "$tunnel_id" ]; then
        echo "Tunnel ${TUNNEL_NAME} not found. Run: $0 setup" >&2
        exit 1
    fi
    cat <<EOF
Cloudflare DNS for tunnel ${TUNNEL_NAME} (${tunnel_id})

Auto route failed because meanly.one / www.meanly.one already have DNS records.
Edit them in Cloudflare → DNS → Records:

  Type    Name   Content                                      Proxy
  CNAME   @      ${tunnel_id}.cfargotunnel.com                Proxied (orange)
  CNAME   www    ${tunnel_id}.cfargotunnel.com                Proxied (orange)

Delete conflicting A/AAAA records for @ and www first.

Or: Zero Trust → Networks → Tunnels → ${TUNNEL_NAME} → Public Hostname
    add meanly.one and www.meanly.one → http://127.0.0.1:${DEV_PORT}

Then:
  cd frontend && npm run dev -- -H 127.0.0.1 -p ${DEV_PORT}
  MEANLY_DEV_PROFILE=${DEV_PROFILE} $0 run

Skip "cloudflared tunnel login" if ~/.cloudflared/cert.pem already exists.
EOF
}

usage() {
    cat <<EOF
Meanly dev — Cloudflare named tunnel

Usage: $(basename "$0") <command>

Environment:
  MEANLY_DEV_PROFILE       global | ru | all (default: global)
  MEANLY_DEV_TUNNEL_HOST   Primary hostname for global/ru profile
  MEANLY_DEV_TUNNEL_NAME   Tunnel name (default: meanly-dev)
  MEANLY_DEV_PORT          Local Next port (default: 3001)

Profiles:
  global   meanly.one + www.meanly.one
  ru       meanly.ru + www.meanly.ru
  all      all four hostnames on one local Next port

Commands:
  write-config   Render ${CONFIG_FILE}
  sync-env       Patch frontend/.env for the active profile
  setup          tunnel create + route dns + config + sync-env
  run            Start tunnel (keep terminal open)
  status         Show tunnel and local health
  dns-help       Show manual Cloudflare DNS steps when auto-route fails

Beta Telegram notify (optional):
  cp scripts/.beta-notify.env.example scripts/.beta-notify.env
  ./scripts/dev-tunnel-notify.sh test
  ./scripts/dev-tunnel-notify.sh watch
  MEANLY_BETA_NOTIFY=1 $0 run

Quick start:
  cloudflared tunnel login
  ./scripts/regional-frontend-env.sh global
  MEANLY_DEV_PROFILE=global $0 setup
  cd frontend && npm run dev -- -H 127.0.0.1 -p 3001
  $0 run

RU profile:
  ./scripts/regional-frontend-env.sh ru
  MEANLY_DEV_PROFILE=ru $0 setup

Both domains on one dev server:
  MEANLY_DEV_PROFILE=all $0 setup
EOF
}

case "${1:-}" in
    write-config) cmd_write_config ;;
    sync-env) cmd_sync_env ;;
    setup) cmd_setup ;;
    run) cmd_run ;;
    status) cmd_status ;;
    dns-help) cmd_dns_help ;;
    *) usage; exit 1 ;;
esac
